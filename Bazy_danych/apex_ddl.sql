CREATE TABLE KOLIZJA (
    ID_KOLIZJI     INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_KOLIZJI PRIMARY KEY,
