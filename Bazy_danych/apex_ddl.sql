CREATE OR REPLACE TRIGGER TRG_UPDATE_ILOSC_POJAZDOW
AFTER INSERT OR DELETE OR UPDATE ON SAMOCHOD
FOR EACH ROW
DECLARE
    v_operation VARCHAR2(10);
BEGIN
    IF INSERTING THEN
        v_operation := 'INSERT';
        -- Wywołanie procedury przy wstawieniu pojazdu
        update_ilosc_pojazdow(:NEW.STATUS, NULL, :NEW.ID_PLACOWKI, NULL, v_operation);
    ELSIF DELETING THEN
        v_operation := 'DELETE';
        -- Wywołanie procedury przy usuwaniu pojazdu
        update_ilosc_pojazdow(NULL, :OLD.STATUS, NULL, :OLD.ID_PLACOWKI, v_operation);
    ELSIF UPDATING THEN
        v_operation := 'UPDATE';
        -- Wywołanie procedury przy aktualizacji pojazdu
        update_ilosc_pojazdow(:NEW.STATUS, :OLD.STATUS, :NEW.ID_PLACOWKI, :OLD.ID_PLACOWKI, v_operation);

    END IF;
END;
