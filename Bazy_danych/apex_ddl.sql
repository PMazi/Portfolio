CREATE OR REPLACE TRIGGER TRG_SAMOCHOD_STATUS
AFTER INSERT ON TRANSAKCJA
FOR EACH ROW
DECLARE
    v_status VARCHAR2(20);
    v_id_pojazdu NUMBER;
    v_counter NUMBER;
BEGIN

    
    -- wywola sie exception no_data_found gdy nie ma takiego id_pojazdu
    SELECT ID_POJAZDU INTO v_id_pojazdu
    FROM SAMOCHOD 
    WHERE ID_POJAZDU = :NEW.ID_POJAZDU;

    SELECT STATUS INTO v_status 
    FROM SAMOCHOD 
    WHERE ID_POJAZDU = :NEW.ID_POJAZDU;

    

    IF v_status != 'w salonie' THEN
        RAISE_APPLICATION_ERROR(-20007, 'Nie mozesz dokonac transakcji samochodu, ktory nie znajduje sie w salonie');

    ELSIF :NEW.STATUS_TRANSAKCJI = 'sprzedane' THEN
        UPDATE SAMOCHOD
        SET STATUS = 'sprzedane'
        WHERE ID_POJAZDU = :NEW.ID_POJAZDU;

        INSERT INTO PLATNOSC (ID_TRANSAKCJI)
        VALUES(:NEW.ID_TRANSAKCJI);

    ELSIF :NEW.STATUS_TRANSAKCJI = 'wypozyczone' THEN
        UPDATE SAMOCHOD
        SET STATUS = 'wypozyczone'
        WHERE ID_POJAZDU = :NEW.ID_POJAZDU;
        
        INSERT INTO UBEZPIECZENIE (ID_TRANSAKCJI, DATA_ZAWARCIA)
        VALUES(:NEW.ID_TRANSAKCJI, NULL);

    ELSE
        RAISE_APPLICATION_ERROR(-20010, 'Wystapil blad podczas wstawniania pojazdu');
    END IF;
    
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20008, 'Nie ma pojazdu o podanym przez ciebie ID ' || :NEW.ID_POJAZDU);
    
END;
/



CREATE OR REPLACE TRIGGER TRG_DELETE_TRANSAKCJA
BEFORE DELETE ON TRANSAKCJA
FOR EACH ROW
BEGIN

    IF :OLD.STATUS_TRANSAKCJI = 'wypozyczone' OR :OLD.STATUS_TRANSAKCJI = 'koniec wypozyczenia' THEN
        UPDATE KLIENT
            SET WYDATKI = WYDATKI - (SELECT KWOTA_ZAPLACONA FROM PLATNOSC 
                                     WHERE ID_TRANSAKCJI = :OLD.ID_TRANSAKCJI)
                                  - (SELECT CENA_UBEZPIECZENIA FROM UBEZPIECZENIE
                                     WHERE ID_TRANSAKCJI = :OLD.ID_TRANSAKCJI)
        WHERE ID_KLIENTA = :OLD.ID_KLIENTA;
    ELSE
        UPDATE KLIENT
            SET WYDATKI = WYDATKI - (SELECT KWOTA_ZAPLACONA FROM PLATNOSC 
                                     WHERE ID_TRANSAKCJI = :OLD.ID_TRANSAKCJI)
        WHERE ID_KLIENTA = :OLD.ID_KLIENTA;
    END IF;

    IF :OLD.STATUS_TRANSAKCJI = 'wypozyczone' THEN
        DELETE FROM UBEZPIECZENIE 
        WHERE ID_TRANSAKCJI = :OLD.ID_TRANSAKCJI;
    END IF;

    DELETE FROM PLATNOSC 
    WHERE ID_TRANSAKCJI = :OLD.ID_TRANSAKCJI;
END;
/


