CREATE OR REPLACE TRIGGER TRG_SET_KWOTA_ZAPLACONA
BEFORE UPDATE ON PLATNOSC
FOR EACH ROW
DECLARE
    V_STATUS_TRANSAKCJI VARCHAR2(30);
    V_CENA NUMBER(10,2);
    V_CZAS_TRWANIA_W_MIESIACACH NUMBER(10,2);
    V_RODZAJ_PLATNOSCI VARCHAR(255);
BEGIN

    V_RODZAJ_PLATNOSCI := :OLD.RODZAJ_PLATNOSCI;

    IF V_RODZAJ_PLATNOSCI = 'karta platnicza' OR V_RODZAJ_PLATNOSCI = 'gotowka' THEN
        RETURN;
    END IF;

    SELECT T.STATUS_TRANSAKCJI
    INTO V_STATUS_TRANSAKCJI
    FROM TRANSAKCJA T
    WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI;

    IF V_STATUS_TRANSAKCJI = 'sprzedane' THEN
        SELECT S.CENA_ZA_KUPNO
        INTO V_CENA
        FROM SAMOCHOD S JOIN TRANSAKCJA T ON T.ID_POJAZDU = S.ID_POJAZDU
        WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI;

    ELSIF V_STATUS_TRANSAKCJI = 'wypozyczone' THEN
        SELECT S.CENA_ZA_WYNAJEM
        INTO V_CENA
        FROM SAMOCHOD S JOIN TRANSAKCJA T ON T.ID_POJAZDU = S.ID_POJAZDU
        WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI;
    ELSE
        RAISE_APPLICATION_ERROR(-20001, 'Blad podczas platnosci');
    END IF;
    
    
    IF V_STATUS_TRANSAKCJI = 'wypozyczone' THEN
        SELECT czas_trwania_w_miesiacach INTO V_CZAS_TRWANIA_W_MIESIACACH from ubezpieczenie where id_transakcji = :new.id_transakcji;

        V_CENA := V_CENA * V_CZAS_TRWANIA_W_MIESIACACH;
    END IF;

    :NEW.KWOTA_ZAPLACONA := V_CENA;

    UPDATE KLIENT
        SET WYDATKI = NVL(WYDATKI, 0) + V_CENA
        WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                            FROM TRANSAKCJA T 
                            WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);

END;
/


CREATE OR REPLACE TRIGGER TRG_SET_CENA_UBEZPIECZENIA
BEFORE UPDATE OF NAZWA ON UBEZPIECZENIE
FOR EACH ROW
DECLARE
    CENA_KUPNA NUMBER(10, 2);
    STATUS VARCHAR(30);
BEGIN

    IF :NEW.STATUS_UBEZPIECZENIA = 'nieaktywne' THEN
        RETURN;
    END IF;
   
    SELECT S.CENA_ZA_KUPNO, T.STATUS_TRANSAKCJI
    INTO CENA_KUPNA, STATUS
    FROM SAMOCHOD S JOIN TRANSAKCJA T ON S.ID_POJAZDU = T.ID_POJAZDU
    WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI;

    IF STATUS = 'sprzedane' THEN
        IF :NEW.NAZWA = 'OC' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 1;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 1
            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);

        ELSIF :NEW.NAZWA = 'Assistance' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 2;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 2
            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);

        ELSIF :NEW.NAZWA = 'AC' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 3;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.005 * CENA_KUPNA * 3

            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);
        ELSE
            RAISE_APPLICATION_ERROR(-20001, 'Niepoprawna nazwa ubezpieczenia');
        END IF;
    END IF;
    
    IF STATUS = 'wypozyczone' THEN
        IF :NEW.NAZWA = 'OC' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 1;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 1
            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);

        ELSIF :NEW.NAZWA = 'Assistance' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 2;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 2
            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);

        ELSIF :NEW.NAZWA = 'AC' THEN
            :NEW.CENA_UBEZPIECZENIA := :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 3;
            UPDATE KLIENT
            SET WYDATKI = NVL(WYDATKI, 0) + :NEW.CZAS_TRWANIA_W_MIESIACACH * 0.0005 * CENA_KUPNA * 3
            WHERE ID_KLIENTA = (SELECT T.ID_KLIENTA 
                                FROM TRANSAKCJA T 
                                WHERE T.ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI);
        ELSE
            RAISE_APPLICATION_ERROR(-20001, 'Niepoprawna nazwa ubezpieczenia');
        END IF;

        :NEW.STATUS_UBEZPIECZENIA := 'aktywne';
        :NEW.DATA_ZAKONCZENIA := ADD_MONTHS(:NEW.DATA_ZAWARCIA, :NEW.CZAS_TRWANIA_W_MIESIACACH);

    END IF;

    INSERT INTO PLATNOSC (ID_TRANSAKCJI)
        VALUES(:NEW.ID_TRANSAKCJI);
END;
/


CREATE OR REPLACE TRIGGER TRG_STATUS_UBEZPIECZENIA
BEFORE UPDATE OF STATUS_UBEZPIECZENIA ON UBEZPIECZENIE
FOR EACH ROW
BEGIN
    IF :NEW.STATUS_UBEZPIECZENIA = 'nieaktywne' THEN
        UPDATE TRANSAKCJA
            SET STATUS_TRANSAKCJI = 'koniec wypozyczenia'
        WHERE ID_TRANSAKCJI = :NEW.ID_TRANSAKCJI;
    END IF;
END;
/


CREATE OR REPLACE TRIGGER TRG_VALIDATE_PRACOWNIK_PENSJE
BEFORE INSERT OR UPDATE ON PRACOWNIK
FOR EACH ROW
DECLARE
    v_min_placa  NUMBER;
    v_max_placa  NUMBER;
BEGIN
    SELECT min_placa, max_placa
    INTO v_min_placa, v_max_placa
    FROM STANOWISKO
    WHERE NAZWA_STANOWISKA = :NEW.NAZWA_STANOWISKA;

    IF :NEW.PENSJA NOT BETWEEN v_min_placa AND v_max_placa THEN
        RAISE_APPLICATION_ERROR(-20002, 'Pensja musi mieścić się w zakresie od ' || v_min_placa || ' do ' || v_max_placa || ' dla stanowiska: ' || :NEW.NAZWA_STANOWISKA);
    END IF;
END;
/


CREATE OR REPLACE TRIGGER TRG_NAZWA_PLACOWKI
BEFORE INSERT ON PLACOWKA
FOR EACH ROW
DECLARE
    v_count INTEGER;
BEGIN
    
    SELECT COUNT(*)
    INTO v_count
    FROM PLACOWKA
    WHERE MIASTO = :NEW.MIASTO
    AND ADRES = :NEW.ADRES;

    
    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20003, 'Placowka znajduje sie juz w wybranej lokalizacji');
    END IF;
END;
/


CREATE OR REPLACE TRIGGER TRG_NAZWA_STANOWISKA
BEFORE INSERT ON STANOWISKO
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM STANOWISKO 
    WHERE NAZWA_STANOWISKA = :NEW.NAZWA_STANOWISKA;
    
    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20004, 'Podana nazwa stanowiska ' || :NEW.NAZWA_STANOWISKA || ' już istnieje');
    END IF;

END;
/


