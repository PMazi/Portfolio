CREATE TABLE PRACOWNIK (
    ID_PRACOWNIKA      INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_PRACOWNIKA PRIMARY KEY,
    IMIE               VARCHAR2(20) NOT NULL,
    NAZWISKO           VARCHAR2(30) NOT NULL,
