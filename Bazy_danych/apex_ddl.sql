    NAZWA_STANOWISKA   VARCHAR2(50) NOT NULL,
    ID_PLACOWKI        INTEGER NOT NULL,
    FOREIGN KEY (ID_PLACOWKI) REFERENCES PLACOWKA (ID_PLACOWKI),
    FOREIGN KEY (NAZWA_STANOWISKA) REFERENCES STANOWISKO (NAZWA_STANOWISKA)
);

CREATE INDEX IDX_PENSJA ON PRACOWNIK(PENSJA);


