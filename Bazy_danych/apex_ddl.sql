    UPDATE PRACOWNIK
        SET NAZWA_STANOWISKA = :NEW.NAZWA_STANOWISKA
        WHERE NAZWA_STANOWISKA = :OLD.NAZWA_STANOWISKA;
    EXCEPTION
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20005, 'Nie możesz modyfikowac nazwy stanowiska w ktorej sa pracownicy');
            NULL;
END;
/


CREATE OR REPLACE TRIGGER TRG_SERWIS_ID_POJAZDU
BEFORE INSERT ON SERWIS
FOR EACH ROW
DECLARE
    v_id_pojazdu NUMBER;
BEGIN
    SELECT ID_POJAZDU INTO v_id_pojazdu
    FROM SAMOCHOD
    WHERE ID_POJAZDU = :NEW.ID_POJAZDU;

    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20008, 'Nie ma pojazdu o podanym przez ciebie ID ' || :NEW.ID_POJAZDU);
END;
/


