CREATE OR REPLACE FUNCTION update_ilosc_pracownikow (
    p_id_placowki IN PRACOWNIK.ID_PLACOWKI%TYPE,
    p_old_id_placowki IN PRACOWNIK.ID_PLACOWKI%TYPE,
    p_operation IN VARCHAR2
) RETURN VARCHAR2 IS
BEGIN
    IF p_operation = 'INSERT' THEN
        -- Dodanie pracownika do placówki
        UPDATE PLACOWKA
        SET ILOSC_PRACOWNIKOW = ILOSC_PRACOWNIKOW + 1
        WHERE ID_PLACOWKI = p_id_placowki;
    
    ELSIF p_operation = 'DELETE' THEN
        -- Usunięcie pracownika z placówki
        UPDATE PLACOWKA
        SET ILOSC_PRACOWNIKOW = ILOSC_PRACOWNIKOW - 1
        WHERE ID_PLACOWKI = p_old_id_placowki;
    
    ELSIF p_operation = 'UPDATE' THEN
        -- Zmiana placówki dla pracownika
        IF p_old_id_placowki != p_id_placowki THEN
        
            UPDATE PLACOWKA
            SET ILOSC_PRACOWNIKOW = ILOSC_PRACOWNIKOW - 1
            WHERE ID_PLACOWKI = p_old_id_placowki;

            UPDATE PLACOWKA
            SET ILOSC_PRACOWNIKOW = ILOSC_PRACOWNIKOW + 1
            WHERE ID_PLACOWKI = p_id_placowki;
        END IF;
    END IF;

    RETURN 'OK';
END;
/


CREATE OR REPLACE TRIGGER TRG_UPDATE_ILOSC_PRACOWNIKOW
AFTER INSERT OR DELETE OR UPDATE ON PRACOWNIK
FOR EACH ROW
DECLARE
    v_result VARCHAR2(100);
BEGIN
    IF INSERTING THEN
        v_result := update_ilosc_pracownikow(:NEW.ID_PLACOWKI, NULL, 'INSERT');
    ELSIF DELETING THEN
        v_result := update_ilosc_pracownikow(NULL, :OLD.ID_PLACOWKI, 'DELETE');
    ELSIF UPDATING THEN
        v_result := update_ilosc_pracownikow(:NEW.ID_PLACOWKI, :OLD.ID_PLACOWKI, 'UPDATE');
    END IF;
END;
/



CREATE OR REPLACE PROCEDURE update_ilosc_pojazdow (
p_status IN SAMOCHOD.STATUS%TYPE,
p_old_status IN SAMOCHOD.STATUS%TYPE,
p_id_placowki IN SAMOCHOD.ID_PLACOWKI%TYPE,
p_old_id_placowki IN SAMOCHOD.ID_PLACOWKI%TYPE,
p_operation IN VARCHAR2
) IS
BEGIN
    IF p_operation = 'INSERT' THEN
        IF p_status = 'w salonie' then
            -- Dodanie pojazdu do placówki
            UPDATE PLACOWKA
            SET ILOSC_POJAZDOW = ILOSC_POJAZDOW + 1
            WHERE ID_PLACOWKI = p_id_placowki;
        END IF;

    ELSIF p_operation = 'DELETE' THEN
        IF p_old_status = 'w salonie' THEN
            -- Usunięcie pojazdu z placówki
            UPDATE PLACOWKA
            SET ILOSC_POJAZDOW = ILOSC_POJAZDOW - 1
            WHERE ID_PLACOWKI = p_old_id_placowki;
        END IF;

    ELSIF p_operation = 'UPDATE' THEN

        IF p_old_id_placowki = p_id_placowki THEN
            IF p_old_status = 'w salonie' AND p_status != 'w salonie' THEN
                UPDATE PLACOWKA
                SET ILOSC_POJAZDOW = ILOSC_POJAZDOW - 1
                WHERE ID_PLACOWKI = p_id_placowki;
            END IF;

            ELSE IF p_old_status != 'w salonie' AND p_status = 'w salonie' THEN
                UPDATE PLACOWKA
                SET ILOSC_POJAZDOW = ILOSC_POJAZDOW + 1
                WHERE ID_PLACOWKI = p_id_placowki;
            END IF;
        END IF;
        -- Zmiana placówki dla pojazdu
        IF p_old_id_placowki != p_id_placowki THEN

            IF p_status = 'w salonie' THEN
                UPDATE PLACOWKA
                SET ILOSC_POJAZDOW = ILOSC_POJAZDOW - 1
                WHERE ID_PLACOWKI = p_old_id_placowki;

                UPDATE PLACOWKA
                SET ILOSC_POJAZDOW = ILOSC_POJAZDOW + 1
                WHERE ID_PLACOWKI = p_id_placowki;
            END IF;
            
        END IF;
    END IF;
END;
/


