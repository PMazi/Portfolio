    ROK_PRODUKCJI        INTEGER NOT NULL,
    STATUS               VARCHAR2(30) DEFAULT 'w salonie'
                          CONSTRAINT CHK_STATUS_SAMOCHOD 
                          CHECK (STATUS IN ('w salonie', 'sprzedane', 'wypozyczone')),
    CENA_ZA_KUPNO        NUMBER(10,2),
    CENA_ZA_WYNAJEM      NUMBER(10,2),
    ID_PLACOWKI          INTEGER,
    FOREIGN KEY (ID_PLACOWKI) REFERENCES PLACOWKA (ID_PLACOWKI)
);



CREATE INDEX IDX_CENA_ZA_KUPNO ON SAMOCHOD(CENA_ZA_KUPNO);

