CREATE TABLE STANOWISKO (
    ID_STANOWISKA INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_STANOWISKA PRIMARY KEY,
    NAZWA_STANOWISKA VARCHAR2(50) NOT NULL UNIQUE,
    MAX_PLACA        NUMBER(10,2) NOT NULL,
    MIN_PLACA        NUMBER(10,2) NOT NULL
);


CREATE TABLE KLIENT (
    ID_KLIENTA          INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_KLIENTA PRIMARY KEY,
    IMIE                VARCHAR2(30) NOT NULL,
    NAZWISKO            VARCHAR2(30) NOT NULL,
    WIEK                INTEGER NOT NULL,
    WYDATKI             NUMBER(10,2) DEFAULT 0    
);


CREATE TABLE PLACOWKA (
    ID_PLACOWKI       INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_PLACOWKI PRIMARY KEY,
    MIASTO            VARCHAR2(30) NOT NULL,
    ADRES             VARCHAR2(30) NOT NULL,
    ILOSC_PRACOWNIKOW INTEGER DEFAULT 0,
    ILOSC_POJAZDOW    INTEGER DEFAULT 0
);


CREATE TABLE SAMOCHOD (
    ID_POJAZDU           INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_POJAZDU PRIMARY KEY,
