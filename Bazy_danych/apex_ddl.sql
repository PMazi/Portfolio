CREATE TABLE TRANSAKCJA (
    ID_TRANSAKCJI       INTEGER GENERATED ALWAYS AS IDENTITY START WITH 1 INCREMENT BY 1 NOCACHE CONSTRAINT PK_ID_TRANSAKCJI PRIMARY KEY,
    DATA_TRANSAKCJI     DATE NOT NULL,
    STATUS_TRANSAKCJI   VARCHAR2(30) NOT NULL
                          CONSTRAINT CHK_STATUS_TRANSAKCJI 
                          CHECK (STATUS_TRANSAKCJI IN ('sprzedane', 'wypozyczone', 'koniec wypozyczenia')),
    ID_POJAZDU          INTEGER NOT NULL,
    ID_KLIENTA          INTEGER NOT NULL,
    FOREIGN KEY (ID_KLIENTA) REFERENCES KLIENT (ID_KLIENTA),
    FOREIGN KEY (ID_POJAZDU) REFERENCES SAMOCHOD (ID_POJAZDU)
